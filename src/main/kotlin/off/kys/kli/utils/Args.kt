package off.kys.kli.utils

import off.kys.kli.parser.ArgParser

/**
 * Type alias for [ArgParser], used to represent the parsed arguments.
 *
 * This type alias makes it easier to refer to the [ArgParser] class within the project, as it allows
 * you to use `Args` instead of `ArgParser`. This simplifies the code and enhances readability when dealing
 * with argument parsing.
 *
 * @see ArgParser
 */
typealias Args = ArgParser
